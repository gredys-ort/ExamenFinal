package DepJutiapa;

/**
 *
 * @author GredysMorales
 */
public class MuniJutiapa {

    public String MuniJutiapa(char cr) {
        String munis = "";
        switch (cr) {
            case 'a':
                munis = "Agua Blanca";
                break;
            case 'b':
                munis = "Santa Catarina Mita";
                break;
            case 'c':
                munis = "Asuncion Mita";
                break;
            case 'd':
                munis = "El Progreso";
                break;
            case 'e':
                munis = "Atescatempa";
                break;
            case 'f':
                munis = "Conguaco";
                break;
            case 'g':
                munis = "El Adelanto";
                break;
            case 'h':
                munis = "Zapotitlan";
                break;
            case 'i':
                munis = "Yupiltepeque";
                break;
            case 'j':
                munis = "Pasaco";
                break;
            case 'k':
                munis = "Moyuta";
                break;
            case 'l':
                munis = "San Jose Acatempa";
                break;
            case 'm':
                munis = "Quezada";
                break;
            case 'n':
                munis = "Jutiapa";
                break;
            case 'o':
                munis = "Jerez";
                break;
            case 'p':
                munis = "Comapa";
                break;
            case 'q':
                munis = "Jalpatagua";
                break;

        }
        return munis;
    }
}
